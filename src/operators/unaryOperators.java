package operators;

public class unaryOperators {

	public static void main(String[] args) {
		int a=20,
		 b=30 ,c=15;
					
				//Unary operator 
				System.out.println(a++); //post incremental
				System.out.println(++a); //pre-incremental
				
				System.out.println(a--); //post decremental
				//System.out.println(a);
				System.out.println(--a); //pre-dicremental 
				
				
				System.out.println(a>b && b>c);//false

	}

}
